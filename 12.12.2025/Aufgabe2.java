// Dieses Programm demonstriert verschiedene Rechenoperationen.

public class Aufgabe2 {
    public static void main(String[] args) {
        
        // Gibt das Ergebnis der Berechnung aus

        System.out.println(19+5); // Addiert zwei Zahlen
        System.out.println(19-5); // Subtrahiert die zweite Zahl von der ersten
        System.out.println(19*5); // Multipliziert die zwei Zahlen
        System.out.println(19/5); // Dividiert die beiden Zahlen und rundet ab
        System.out.println(19%5); // Berechnet den Rest / Modulo
        System.out.println(19./5.); // Dividiert die beiden Zahlen und gibt das Ergebnis als float / Dezimalzahl aus
    }
}
