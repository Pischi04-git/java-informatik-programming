public class Aufgabe5 {
    public static void main(String[] args) {
        int ersteZahl = 1; // Deklaration und Initialisierung der ersten Zahl
        int zweiteZahl = 2; // Deklaration und Initialisierung der zweiten Zahl
        boolean vergleich = ersteZahl <= zweiteZahl; // Vergleich der beiden Zahlen, falls die erste Zahl kleiner oder gleich der zweiten Zahl ist wird die Variable Vergleich auf Wahr gesetzt
        System.out.println(vergleich); // Ausgabe des Vergleichsergebnisses
    }
}