public class dreikontrollstrukturen {
    public static void main(String[] args) {
        int zahl = 5;
        if (zahl > 7) {
            System.out.println("Die Zahl ist größer als 7");
        } else if (zahl == 7) {
            System.out.println("Die Zahl ist gleich 7");
        } else {
            System.out.println("Die Zahl ist kleiner als 7");
        }
    }
}
